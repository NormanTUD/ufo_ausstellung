<p>Auf diesen Bildern sind verschiedene von Raketen verursachte
Phänomene zu sehen. Die Spiralen sind sogenannte Raketenspiralen. Sie
entstehen auf zwei verschiedene Weisen:</p>
<ul>
<li><p>Eine der Hauptursachen für das Auftreten von Raketenspiralen ist
die Eigenrotation der Rakete zur Stabilisierung. Diese Rotation führt
dazu, dass die Abgase spiralförmig abgegeben werden, die sich dann im
Weltraum ausbreiten. Die Abgase können sich aufgrund der fehlenden
Atmosphäre bis zu 250 km groß entfalten, da im Weltraum keine Luft oder
andere Hindernisse vorhanden sind, die die Ausbreitung
behindern.</p>

</li>
<li><p>Eine zweite Möglichkeit sind Fehlstarts. Die Rakete spiralt  sich
dabei um sich selbst und stößt weiterhin Abgase ab, die ebenfalls
spiralförmig sich ausbreiten.</p>

</li>
</ul>
<p>In beiden Fällen entsteht eine sichtbare Spirale, die von der Sonne
beleuchtet wird und dadurch besonders auffällig ist.</p>
<p>Die bekannteste Spiralformation vom 9. Dezember 2009 wurde von
zahlreichen Beobachtern in den nördlichen Regionen Norwegens sowie in
Teilen Schwedens gesehen. Der offizielle Grund wurde als gescheiterter
Test eines russischen Bulava-Raketenmissils (ICBM) erklärt, bei dem die
dritte Stufe in großer Höhe versagte. Trotz dieser Erklärung wurde
weiterhin über die Herkunft und Natur des Phänomens spekuliert. Die
Spirale wurde in einer Höhe zwischen 107 und 166 Meilen (172 bis 267 km)
beobachtet und erreichte beim Endstadium eine Breite von etwa 629
km.</p>
<p>Das Phänomen wies eine unerwartete Symmetrie in den Farben und der
Geometrie auf, was zu Spekulationen über alternative Erklärungen führte,
darunter UFOs und HAARP-ähnliche Energievortex. Während viele Theorien
diskutiert wurden, zeigte sich, dass das Spektakel eine hochentwickelte
und koordinierte Erscheinung war, die durch die Umstände des
misslungenen Raketentests hervorgerufen wurde. Interessanterweise fand
die Expansion des spiralförmigen Phänomens mit einer Geschwindigkeit von
über 12,000 km/h) statt, was auf
eine immense Dynamik hindeutet.</p>
<p>Eine der bekanntesten Behauptungen kam von David Wilcock, der die
EISCAT-Einrichtung in Norwegen mit dem Phänomen in Verbindung brachte
und darauf hinwies, dass die Symmetrie der Spirale die Theorie eines
misslungenen Raketenstarts in Frage stellt.</p>
