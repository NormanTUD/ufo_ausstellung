<p>Der Begriff <em>Space Jellyfish</em> (auch bekannt als <em>Jellyfish
UFO</em> oder <em>Rocket Jellyfish</em>) beschreibt ein Phänomen, das
mit Raketenstarts in Verbindung steht. Es entsteht, wenn Sonnenlicht von
den hochgelegenen Abgasen des Raketenantriebs reflektiert wird, während
die Rakete während der Dämmerung oder Morgendämmerung gestartet wird.
Dabei befindet sich der Beobachter in der Dunkelheit, während die
Abgasfahnen in großer Höhe weiterhin direktem Sonnenlicht ausgesetzt
sind. Diese leuchtende Erscheinung erinnert an eine Qualle.</p>
