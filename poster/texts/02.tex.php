<p>Im März 1971 wurden angeblich von dem US-U-Boot USS Trepang Aufnahmen
durch das Periskop gemacht, die kürzlich für Aufregung unter
UFO-Forschern und Skeptikern sorgten. Die Bilder zeigen verschiedene
Flugobjekte über der Wasseroberfläche, die entweder aus dem Wasser
aufsteigen oder nach Beschuss ins Wasser stürzen. Die UFO-Forscher Alex
Mistretta, John Greenewald Jr. und Steve Murillo haben die Hintergründe
der Bilder untersucht und glauben, eine Erklärung gefunden zu haben.</p>
<p>Mistretta hörte von den Aufnahmen erstmals durch eine
vertrauenswürdige Informationsquelle in Europa, die bestätigte, dass es
sich um eine Fotoserie handelt, die zuvor im französischen Magazin Top
Secret veröffentlicht wurde. Laut dieser anonymen Quelle wurden die
Bilder von Bord der USS Trepang im Arktischen Meer zwischen Island und
der norwegischen Insel Jan Mayen aufgenommen, als Admiral Dean Reynolds
Sackett das Kommando hatte. Der Offizier John Klika soll die Objekte
zufällig mit dem Periskop entdeckt haben.</p>
<p>Die von Mistretta erhaltenen Bilder wiesen jedoch keine Aufschriften
auf, während das Magazin Top Secret behauptete, dass bestimmte Vermerke
zu finden seien. Mistretta erhielt am 13. Juli 2015 Originalabzüge der
Fotoserie ohne diese Aufschriften, die nicht Scans oder Kopien waren.
Recherchen bestätigten, dass sowohl Admiral Sackett als auch Klika zur
fraglichen Zeit auf der Trepang waren, doch beide berichteten, nichts
Ungewöhnliches gesehen zu haben. Klika äußerte, dass er nicht wisse, um
was es sich bei den Objekten auf den Bildern handeln könnte.</p>
<p>Greenewald stellte fest, dass die Bilder möglicherweise Zielballons
der Marine zeigen, die während Waffentests verwendet wurden. Offizielle
Marineunterlagen belegen, dass zwischen dem 22. Februar und dem 22. März
1971 U-Boote unter der nördlichen Eiskappe Tests zur Datengewinnung für
Waffensysteme durchführten. Historische Aufnahmen von Ballon-Trägern aus
den Jahren 1910-1915 zeigen ähnliche Objekte, was die Theorie
unterstützt, dass es sich um militärische Testziele handelt.</p>
<p>Greenewald vermutet, dass die Aufnahmen wahrscheinlich Teil von
Waffentests waren und die UFO-artigen Objekte als Testziele dienten,
ähnlich denen, die in der frühen Luftfahrt eingesetzt wurden. Diese
Ereignisse verdeutlichen, wie militärische Aktivitäten leicht zu
Spekulationen über UFOs führen können, und werfen Fragen zur Art und
Weise auf, wie Informationen über solche Vorfälle vermittelt und
interpretiert werden.</p>
