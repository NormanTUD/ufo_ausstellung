<li><p>Burkhard Heim: ein deutscher Chemiker und Physiker, der während
Forschungen an Sprengstoffen schwer verletzt wurde, aber überlebt hat.
Er wollte eine zwölfdimensionale Alternative zur Relativitätstheorie
schaffen, die angeblich dafür benutzt werden kann, um interstellare
Reisen zu ermöglichen. Er driftete immer mehr ins Esoterische ab, und
wird von der akademischen Physik nicht ernstgenommen. Seine Formeln
liefern keine Einsichten, sondern sind nur anhand der damals bekannten
Naturkonstanten gefittete Formelwerte, die ohne mentales Modell
auskommen. Seine Sprache ist extrem unverständlich, und seine Schriften,
vorallem die esoterischen, kaum mehr lesbar.</p>
		\qr[\wikipedia{} Burkhard Heim]{https://de.wikipedia.org/wiki/Burkhard_Heim}

