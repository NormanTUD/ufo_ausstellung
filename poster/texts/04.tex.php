<li><p>Betty und Barney Hill: sie fuhren auf einer Landstraße nachts,
als angeblich vor ihnen ein helles Licht auftauchte. Darauf war einige
Zeit wie verschwunden, und sie fuhren weiter. Später stellte sich in
Hypnosesitzungen heraus, dass sie angeblich beide entführt worden sind.
Die Sitzungen waren nicht getrennt, Barney, der anfangs keine Aliens
gesehen haben will, hat die Erzählungen seiner Frau gehört und erst
daraufhin selbst von Außerirdischen geredet, die er gesehen haben will.
Der Fall wurde nie final aufgelöst, aber es war höchstwahrscheinlich
eine Form von Folie à deux, wo eine Person sich in eine Psychose
reinredet, und eine andere, ihr nahestendende Person sich mit
hineinsaugen lässt.</p>
