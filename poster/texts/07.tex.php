<p><em>Von oben nach unten und links nach rechts</em>:</p>
<p>Die roten Blitze sind sogenannte Sprites. Sie sind höchst selten,
sehr groß (bis einige hundert Kilometer groß) und sehr kurzlebig, meist
nur einige Millisekunden lang. Sprites entstehen in der oberen
Atmosphäre, typischerweise über Gewitterwolken, und sind das Ergebnis
von elektrischen Entladungen zwischen der Atmosphäre und der Ionosphäre.
Lange Zeit waren sie als Gerücht unter Piloten bekannt, die diese roten
Blitze gesehen haben wollten, aber niemand wollte ihnen glauben. Erst,
als die ISS sie aufgenommen hat, wurde das Phänomen ernst genommen.
Sprites können in verschiedenen Formen auftreten, einschließlich
diffusen und filiformen Strukturen, und können über 100 Kilometer in die
Höhe reichen.</p>
<p>Links, das Bild mit der Wolke, zeigt einen Crown-Flash. Das ist ein
Lichtstrahl, der sich bewegt, als würde er von einem Suchscheinwerfer
kommen und den Himmel absuchen. In Wirklichkeit ist es Licht, das an
Eiskristallen in der Atmosphäre reflektiert wird. Diese Eiskristalle
richten sich anhand der elektromagnetischen Felder in den Wolken aus und
verändern ihre Position, wenn sich die Felder ändern. Die Reflexion
dieses Lichts erzeugt die charakteristische Erscheinung eines
Crown-Flashes, der oft während heftiger Gewitter auftritt und in
verschiedenen Farben wahrgenommen werden kann.</p>
<p>Darunter ist ein sogenannter Blue Jet, ein spezieller, sehr seltener
Blitztyp, der in die Höhe geht, statt zum Boden. Blue Jets entstehen
ebenfalls in Verbindung mit Gewittern und treten in der oberen
Atmosphäre auf, indem sie bis zu 50 Kilometer hochschießen. Ihre
charakteristische blaue Farbe entsteht durch die Wechselwirkung des
Lichtes mit Molekülen in der Atmosphäre, insbesondere Stickstoff. Diese
Phänomene sind schwer zu beobachten, da sie nur wenige Millisekunden
dauern und in der Regel mit starken Gewittern verbunden sind.</p>
<p>Ganz unten ist eine sogenannte Lentikularwolke. Diese Wolken haben
eine linseähnliche Form und entstehen durch die Störung der
Luftströmung, wenn sie auf Berge oder andere Hindernisse trifft. Sie
sind oft mit stabilen atmosphärischen Bedingungen assoziiert und können
ein Indikator für kommende Wetterwechsel sein. Diese Wolken wurden
vermutlich für die Form vieler UFO-Sichtungen maßgebend, da ihre
ungewöhnliche Form und Erscheinung leicht mit extraterrestrischen
Objekten verwechselt werden können.</p>
